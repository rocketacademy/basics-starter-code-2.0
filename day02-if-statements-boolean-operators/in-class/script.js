var hawkerFoodCategorisationMain = function (input) {
  // Complete the Base: Hawker Food Categorisation exercise below with hawkerFoodCategorisationMain as the main function.

  // by default, food would be other
  var myOutputValue = "this dish is neither rice or noodles";

  // sample dishes for rice
  if (
    input == "chicken rice" ||
    input == "nasi lemak" ||
    input.includes("rice")
  ) {
    myOutputValue = "rice";
  }

  // sample dishes for noodles
  else if (
    input == "hokkien mee" ||
    input == "laksa" ||
    input.includes("noodles") ||
    input.includes("mee")
  ) {
    myOutputValue = "noodles";
  }

  return myOutputValue;
};

